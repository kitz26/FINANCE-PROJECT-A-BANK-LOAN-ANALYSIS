-- PROJECT  QUESTION WITH SOLUTION
-- 1 TOTAL NUMBER OF CUSTOMER
SELECT COUNT(*) AS TOTAL_CUSTOMERS
FROM LOAN_DATA;

-- 2 TOTAL LOAN AMOUNT
SELECT SUM(LOAN_AMOUNT) AS TOTAL_LOAN
FROM LOAN_DATA;

-- 3 AVERAGE INTEREST RATE 
SELECT AVG(INTEREST_RATE) AS AVERAGE_INTEREST
FROM LOAN_DATA;

-- 4 APPROVED LOANS
SELECT * FROM LOAN_DATA
WHERE LOAN_STATUS='APPROVED';

-- 5 REJECTED LOANS
SELECT * FROM LOAN_DATA
WHERE LOAN_STATUS='REJECTED';

-- 6 HIGHEST LOAN AMOUNT
SELECT MAX(LOAN_AMOUNT) 
FROM LOAN_DATA;

-- 7 AVERAGE LOAN AMOUNT BY CITY
SELECT CITY,AVG(LOAN_AMOUNT)
FROM LOAN_DATA
GROUP BY CITY;

-- 8 NUMBER OF APPROVED  LOAN BY CITY
SELECT CITY,COUNT(*) 
FROM LOAN_DATA
WHERE LOAN_STATUS='APPROVED'
GROUP BY CITY;

-- 9 CUSTOMER WITH CREDIT SCORE ABOVE 750
SELECT CUSTOMER_NAME FROM LOAN_DATA
WHERE CREDIT_SCORE>750;

-- 10 TOP 5 HIGHEST INCOME CUSTOMERS
SELECT CUSTOMER_NAME ,MONTHLY_INCOME FROM LOAN_DATA
ORDER BY MONTHLY_INCOME DESC
LIMIT 5;

-- 11 LOAN AMOUNT GREATER THAN 5,00,000
SELECT LOAN_AMOUNT FROM LOAN_DATA
WHERE LOAN_AMOUNT>500000;

-- 12 AVERAGE INCOME OF APPROVED  CUSTOMER
SELECT AVG(MONTHLY_INCOME) FROM LOAN_DATA
WHERE LOAN_STATUS='APPROVED';
